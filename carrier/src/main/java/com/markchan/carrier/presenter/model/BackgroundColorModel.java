public class BackgroundColorModel {

    private final String name;
    private final int color;

    public BackgroundColorModel(String name, int color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public int getColor() {
        return color;
    }
}
